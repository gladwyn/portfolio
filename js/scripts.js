$(document).ready(function() {
    $('#projects').change(function() {
        // Show the corresponding div
        if ($(this).val() == "All"){
            $('.project-container').show();
        } 
        else{
            $('.project-container').hide();
            $('.' + $(this).val()).show();
        }
    });
});
